public class Thuoc {
    private int maNhom;
    private String tenNhom;
    private String maThuoc;
    private String tenThuoc;
    private String dangThuoc;
    private String xuatXu;
    private String canToaBacSi;

    public Thuoc() {
    }

    public int getMaNhom() {
        return maNhom;
    }

    public void setMaNhom(int maNhom) {
        this.maNhom = maNhom;
    }

    public String getTenNhom() {
        return tenNhom;
    }

    public void setTenNhom(String tenNhom) {
        this.tenNhom = tenNhom;
    }

    public String getMaThuoc() {
        return maThuoc;
    }

    public void setMaThuoc(String maThuoc) {
        this.maThuoc = maThuoc;
    }

    public String getTenThuoc() {
        return tenThuoc;
    }

    public void setTenThuoc(String tenThuoc) {
        this.tenThuoc = tenThuoc;
    }

    public String getDangThuoc() {
        return dangThuoc;
    }

    public void setDangThuoc(String dangThuoc) {
        this.dangThuoc = dangThuoc;
    }

    public String getXuatXu() {
        return xuatXu;
    }

    public void setXuatXu(String xuatXu) {
        this.xuatXu = xuatXu;
    }

    public String getCanToaBacSi() {
        return canToaBacSi;
    }

    public void setCanToaBacSi(String canToaBacSi) {
        this.canToaBacSi = canToaBacSi;
    }
    
    
}
