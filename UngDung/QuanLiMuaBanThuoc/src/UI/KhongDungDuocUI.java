    public void hienThiToanBoKhongDungDuoc(){
        KhongDungDuocService khongDungDuocService = new KhongDungDuocService();
        dsKhongDungDuoc = khongDungDuocService.layToanBoKhongDungDuoc();
        
        model.setRowCount(0);
        for (KhongDungDuoc khongDungDuoc : dsKhongDungDuoc) {
            Vector row = new Vector();
            row.add(khongDungDuoc.getMaThuoc() +"_"+khongDungDuoc.getTenThuoc());
            row.add(khongDungDuoc.getMaDoiTuongBenhNhan()+"-"+khongDungDuoc.getTenDoiTuongBenhNhan());
            
            data.add(row);
        }
    }
    
