    private void btbLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btbLuuActionPerformed
        // TODO add your handling code here:
        TrangChuUI trangChuUI = new TrangChuUI();
        KhongDungDuoc khongDungDuoc = new KhongDungDuoc();

        //Ma thuoc
        String maThuoc = trangChuUI.ngatChuoi(cbThuoc.getSelectedItem().toString(), "_", 0);
        khongDungDuoc.setMaThuoc(maThuoc);
        //Ma doi tuong benh nhan
        String maDoiTuongBenhNhan = trangChuUI.ngatChuoi(cbDoiTuongBenhNhan.getSelectedItem().toString() , "-", 0);
        khongDungDuoc.setMaDoiTuongBenhNhan(Integer.parseInt(maDoiTuongBenhNhan));

        KhongDungDuocService khongDungDuocService = new KhongDungDuocService();
        int result = khongDungDuocService.themKhongDungDuoc(khongDungDuoc);
        if (result > 0) {
            hienThiToanBoKhongDungDuoc();
            JOptionPane.showMessageDialog(null, "Thêm thành công");
        }else{
            JOptionPane.showMessageDialog(null, "Thuốc không dùng cho đối tượng bệnh nhân đã được thêm trước đó. Vui lòng kiêm tra lại");
        }
    }//GEN-LAST:event_btbLuuActionPerformed

