    private void btnXoaKhongDungDuocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaKhongDungDuocActionPerformed
        // TODO add your handling code here:
        if (maThuoc == null && maDoiTuongBenhNhan == null) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn thuốc cần xóa");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa?", "Xác nhận", JOptionPane.YES_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        KhongDungDuoc khongDungDuoc = new KhongDungDuoc();
        khongDungDuoc.setMaThuoc(maThuoc);
        khongDungDuoc.setMaDoiTuongBenhNhan(Integer.parseInt(maDoiTuongBenhNhan));

        KhongDungDuocService khongDungDuocService = new KhongDungDuocService();
        int result = khongDungDuocService.xoaKhongDungDuoc(khongDungDuoc);
        if (result > 0) {
            hienThiToanBoKhongDungDuoc();
            JOptionPane.showMessageDialog(null, "Đã xóa");
        }else{
            JOptionPane.showMessageDialog(null, "Xóa thất bại");
        }
    }//GEN-LAST:event_btnXoaKhongDungDuocActionPerformed

