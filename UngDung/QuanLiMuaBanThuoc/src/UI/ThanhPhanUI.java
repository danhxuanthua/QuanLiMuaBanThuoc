    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        TrangChuUI trangChuUI = new TrangChuUI();
        ThanhPhan thanhPhan = new ThanhPhan();
        thanhPhan.setMaThanhPhan(Integer.parseInt(maThanhPhan));

        if (trangChuUI.checkDoDai(txtTenThanhPhanSua.getText(), 1, 50)) {

            thanhPhan.setTenThanhPhan(txtTenThanhPhanSua.getText().trim());
        }else{
            JOptionPane.showMessageDialog(null, "Tên thành phần không hợp lệ");
            return;
        }

        ThanhPhanService thanhPhanService = new ThanhPhanService();
        int result = thanhPhanService.suaThanhPhan(thanhPhan);
        if (result > 0) {
            hienThiToanBoThanhPhan();
            JOptionPane.showMessageDialog(null, "Đã cập nhật");
        }else{
            JOptionPane.showMessageDialog(null, "Thành phần đã tồn tại. Vui lòng kiêm tra lại");
            return;

        }

    }//GEN-LAST:event_btnCapNhatActionPerformed

