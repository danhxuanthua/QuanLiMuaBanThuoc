    private void btnXoaThanhPhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaThanhPhanActionPerformed
        // TODO add your handling code here:
        if (maThanhPhan == null) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn thành phần cần xóa");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa?", "Xác nhận", JOptionPane.YES_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        ThanhPhan thanhPhan = new ThanhPhan();
        thanhPhan.setMaThanhPhan(Integer.parseInt(maThanhPhan));
        ThanhPhanService thanhPhanService = new ThanhPhanService();
        int result = thanhPhanService.xoaThanhPhan(thanhPhan);
        if (result > 0) {
            hienThiToanBoThanhPhan();
            JOptionPane.showMessageDialog(null, "Đã xóa");
        }else{
            JOptionPane.showMessageDialog(null, "Xóa thất bại");
        }
    }//GEN-LAST:event_btnXoaThanhPhanActionPerformed

