    public void hienThiThongKeChiTrongKhoangThoiGian(){
        ArrayList<ThongkeChi> dsThongkeChi = null;
        
        ThongkeChi thongkeChi = new ThongkeChi();
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        
        thongkeChi.setThoiGianBatDau(simpleDateFormat.format(txtThoiGianBatDau.getDate()));
        thongkeChi.setThoiGianKetThuc(simpleDateFormat.format(txtThoiGianKetThuc.getDate()));
        
        ThongKeChiService thongKeChiService = new ThongKeChiService();
        dsThongkeChi = thongKeChiService.layChiTrongMotKhoangThoiGian(thongkeChi);
        
        //fotmat number 
        NumberFormat currentLocale = NumberFormat.getInstance();
        Locale localeVN = new Locale("vi", "VN");
        NumberFormat vn = NumberFormat.getInstance(localeVN);
        
        for (ThongkeChi thongkeChi1 : dsThongkeChi) {
            txtThuChi.append("Tổng chi trong khoảng thời gian từ ngày "+simpleDateFormat.format(txtThoiGianBatDau.getDate())+" đến ngày "+simpleDateFormat.format(txtThoiGianKetThuc.getDate())+" là "+vn.format(thongkeChi1.getTongTienNhap())+" VNĐ\n");
        }
        
    }
    
