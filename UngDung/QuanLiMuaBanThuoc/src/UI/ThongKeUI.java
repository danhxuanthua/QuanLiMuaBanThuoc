    public void hienThiThongKeThangCoDoanhThuCaoNhat(){
        ArrayList<ThongKeThu> dsThongKeThu = null;
        
        ThongKeThuService thongKeThuService = new ThongKeThuService();
        dsThongKeThu = thongKeThuService.layThangCoDoanhThuCaoNhat();
        
        //fotmat number 
        NumberFormat currentLocale = NumberFormat.getInstance();
        Locale localeVN = new Locale("vi", "VN");
        NumberFormat vn = NumberFormat.getInstance(localeVN);
        
        txtThuChi.append("\t\tBÁO CÁO THỐNG KÊ THU CHI\n");
        for (ThongKeThu thongKeThu1 : dsThongKeThu) {
            txtThuChi.append("Tháng có doanh thu cao nhất trong năm là tháng "+thongKeThu1.getThoiGianBatDau()+" với tổng doanh thu "+vn.format(thongKeThu1.getTongTienBan())+" VNĐ\n");
        }
        
    }
    
