    private void tbThuocMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbThuocMouseClicked
        // TODO add your handling code here:
        TrangChuUI trangChuUI = new TrangChuUI();
        int row = tbThuoc.getSelectedRow();
        
        String maNhomVaTenNhom = tbThuoc.getValueAt(row, 0).toString();
        maThuoc = (String) tbThuoc.getValueAt(row, 1);
        String tenThuoc = tbThuoc.getValueAt(row, 2).toString();
        String dangThuoc = (String) tbThuoc.getValueAt(row, 3);
        String xuatXu = (String) tbThuoc.getValueAt(row, 4);
        String canToaBS = (String) tbThuoc.getValueAt(row, 5);
        
        hienThiToanBoNhomThuocSua();
        cbNhomThuocSua.setSelectedItem(maNhomVaTenNhom);
        txtMaThuocSua.setText(maThuoc);
        txtTenThuocSua.setText(tenThuoc);
        txtDangThuocSua.setText(dangThuoc);
        txtXuatXuSua.setText(xuatXu);
        
        if (canToaBS.equals("Cần toa BS")) {
            rdCanToaBacSiSua.setSelected(true);
            rdKhongCanToaBacSiSua.setSelected(false);
        }else if(canToaBS.equals("Không cần toa BS")){
            rdKhongCanToaBacSiSua.setSelected(true);
            rdCanToaBacSiSua.setSelected(false);
        }
    }//GEN-LAST:event_tbThuocMouseClicked
    
