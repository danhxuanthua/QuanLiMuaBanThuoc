    private void btbLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btbLuuActionPerformed
        // TODO add your handling code here:
        TrangChuUI trangChuUI = new TrangChuUI();
        Thuoc thuoc = new Thuoc();
        
        //ma thuoc
        if (trangChuUI.checkDoDai(txtMaThuoc.getText(), 1, 5)) {
            thuoc.setMaThuoc(txtMaThuoc.getText().trim());
        }else{
            JOptionPane.showMessageDialog(null, "Mã thuốc không hợp lệ");
            txtMaThuoc.requestFocus();
            return;
        }
        
        //Ma nhom
        String maNhom = trangChuUI.ngatChuoi((String) cbNhomThuoc.getSelectedItem(), "-", 0);
        thuoc.setMaNhom(Integer.parseInt(maNhom));
        
        //ten thuoc
        if (trangChuUI.checkDoDai(txtTenThuoc.getText(), 1, 50)) {
            thuoc.setTenThuoc(txtTenThuoc.getText().trim());
        }else{
            JOptionPane.showMessageDialog(null, "Tên thuốc không hợp lệ");
            txtTenThuoc.requestFocus();
            return;
        }
        
        //dang thuoc
        if (trangChuUI.checkDoDai(txtDangThuoc.getText(), 1, 50)) {
            thuoc.setDangThuoc(txtDangThuoc.getText().trim());
        }else{
            JOptionPane.showMessageDialog(null, "Dạng thuốc không hợp lệ");
            txtDangThuoc.requestFocus();
            return;
        }
        
        //xuat xu
        thuoc.setXuatXu(txtXuatXua.getText().trim());
        
        //rd can toa bac si
        if (rdCanToaBacSi.isSelected() || rdKhongCanToaBacSi.isSelected()) {
            if (rdCanToaBacSi.isSelected()) {
                thuoc.setCanToaBacSi(rdCanToaBacSi.getText());
            }else if (rdKhongCanToaBacSi.isSelected()) {
                thuoc.setCanToaBacSi(rdKhongCanToaBacSi.getText());
            }
        }else{
            JOptionPane.showMessageDialog(null, "Thuốc cần toa bác sĩ hay không?");
            rdCanToaBacSi.requestFocus();
            return;
        }
        
        ThuocService thuocService = new ThuocService();
        int result = thuocService.themThuoc(thuoc);
        if (result > 0) {
            hienThiToanBoThuoc();
            JOptionPane.showMessageDialog(null, "Thêm thành công");
        }else{
            JOptionPane.showMessageDialog(null, "Thuốc đã tồn tài. Vui lòng kiểm tra lại");
        }

    }//GEN-LAST:event_btbLuuActionPerformed

