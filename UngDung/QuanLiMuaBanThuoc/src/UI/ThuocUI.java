    private void btnXoaThuocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaThuocActionPerformed
        // TODO add your handling code here:
        if (maThuoc == null) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn thuốc cần xóa");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa?", "Xác nhận", JOptionPane.YES_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        Thuoc thuoc = new Thuoc();
        thuoc.setMaThuoc(maThuoc);
        ThuocService thuocService = new ThuocService();
        int result = thuocService.xoaThuoc(thuoc);
        if (result > 0) {
            hienThiToanBoThuoc();
            JOptionPane.showMessageDialog(null, "Đã xóa");
        }else{
            JOptionPane.showMessageDialog(null, "Xóa thất bại");
        }
    }//GEN-LAST:event_btnXoaThuocActionPerformed

