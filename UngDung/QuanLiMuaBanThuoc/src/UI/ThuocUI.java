    ArrayList<Thuoc> dsThuoc = null;
    public void hienThiToanBoThuoc(){
        ThuocService thuocService = new ThuocService();
        dsThuoc = thuocService.layToanBoThongTinThuoc();
        
        model.setRowCount(0);
        for (Thuoc thuoc : dsThuoc) {
            Vector row  = new Vector();
            row.add(thuoc.getMaNhom()+"-"+thuoc.getTenNhom());
            row.add(thuoc.getMaThuoc());
            row.add(thuoc.getTenThuoc());
            row.add(thuoc.getDangThuoc());
            row.add(thuoc.getXuatXu());
            row.add(thuoc.getCanToaBacSi());
            data.add(row);
        }
        
    }
    
