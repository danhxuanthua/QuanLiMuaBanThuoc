    private void btnXoaChiTietHoaDonXuatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaChiTietHoaDonXuatActionPerformed
        // TODO add your handling code here:
        if (maThuoc == null && soChungTuXuat == null) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn thuốc cần xóa");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa?", "Xác nhận", JOptionPane.YES_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        
        ChiTietHoaDonXuat chiTietHoaDonXuat = new ChiTietHoaDonXuat();
        ChiTietHoaDonXuatService chiTietHoaDonXuatService = new ChiTietHoaDonXuatService();
        
        chiTietHoaDonXuat.setSoChungTuXuat(Integer.parseInt(soChungTuXuat));
        chiTietHoaDonXuat.setMaThuoc(maThuoc);
        
        //Cap nhat tong tien cho bang hoa don xuat
        
            //Lay gia ban tu lo
            ArrayList<Lo> dsLo = null;
            Lo lo = new Lo();
            lo.setMaThuoc(maThuoc);
            LoService loService = new LoService();
            dsLo = loService.layGiaBanTheoMaThuoc(lo);
            int giaBan = 0;
            for (Lo lo1 : dsLo) {
                giaBan = lo1.getGiaBan();
            }

            //Tinh gia ban dua vao so luong
            int soLuong = 0;
            int giaBanDuaVaoSoLuong = giaBan * soLuong; 

            //Cap nhat gia ban dua vao so luong vao bang chi tiet hoa don xuat
            dsChiTietHoaDonXuat = chiTietHoaDonXuatService.layChiTietHoaDonXuatCoSoChungTuXuatLonNhat();
            int soChungTuXuatMoiThemGanNhat = 0;

            for (ChiTietHoaDonXuat chiTietHoaDonXuat1 : dsChiTietHoaDonXuat) {
                soChungTuXuatMoiThemGanNhat = chiTietHoaDonXuat1.getSoChungTuXuat();

            }

            chiTietHoaDonXuat.setGiaBanDuaVaoSoLuong(giaBanDuaVaoSoLuong);
            chiTietHoaDonXuat.setSoChungTuXuat(soChungTuXuatMoiThemGanNhat);
            chiTietHoaDonXuat.setMaThuoc(maThuoc);
           
            int x = chiTietHoaDonXuatService.suaGiaBanDuaVaoSoLuong(chiTietHoaDonXuat);
            if (x > 0) {
               capNhatTongTienBanChoHoaDonXuat();
            }
        
        int result = chiTietHoaDonXuatService.xoaChiTietHoaDonXoat(chiTietHoaDonXuat);
        if (result > 0) {
            hienThiToanBoChiTietHoaDonXuat(); 
            JOptionPane.showMessageDialog(null, "Đã xóa");
        }else{
            JOptionPane.showMessageDialog(null, "Xóa thất bại");
            
        }
        
    }//GEN-LAST:event_btnXoaChiTietHoaDonXuatActionPerformed

