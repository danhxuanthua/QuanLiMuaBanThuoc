    public void hienThiSoLuongXuatThuocTheoNgay(){
        
        ArrayList<ThongKeSoLuongXuatNhapThuocTheoNgay> dsXuatNhapThuocTheoNgay = null;
        ThongKeSoLuongXuatNhapThuocTheoNgay thuocTheoNgay = new ThongKeSoLuongXuatNhapThuocTheoNgay();
        
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        thuocTheoNgay.setNgayGioXuat(simpleDateFormat.format(txtNgay.getDate()));
        
        ThongKeSoLuongXuatNhapThuocTheoNgayService thongKeSoLuongXuatNhapThuocTheoNgayService = new ThongKeSoLuongXuatNhapThuocTheoNgayService();
        dsXuatNhapThuocTheoNgay = thongKeSoLuongXuatNhapThuocTheoNgayService.laySoLuongXuatThuocTheoNgay(thuocTheoNgay);

        int tongDoanhThuNgay =0;
        modelXuat.setColumnCount(3);
        modelXuat.setRowCount(0);
        for (ThongKeSoLuongXuatNhapThuocTheoNgay thongKeSoLuongXuatNhapThuocTheoNgay : dsXuatNhapThuocTheoNgay) {
            Vector row = new Vector();
            row.add(thongKeSoLuongXuatNhapThuocTheoNgay.getSoChungTuXuat());
            row.add(thongKeSoLuongXuatNhapThuocTheoNgay.getTenThuoc());
            row.add(thongKeSoLuongXuatNhapThuocTheoNgay.getSoLuongXuat());
            tongDoanhThuNgay = thongKeSoLuongXuatNhapThuocTheoNgay.getTongTienBan();
            dataXuat.add(row);
        }
        
        //fotmat number
        NumberFormat currentLocale = NumberFormat.getInstance();
        Locale localeVN = new Locale("vi", "VN");
        NumberFormat vn = NumberFormat.getInstance(localeVN);
        
        lblTongDoanhThuNgay.setText("Tổng doanh thu ngày "+simpleDateFormat.format(txtNgay.getDate()) + " là: " + vn.format(tongDoanhThuNgay) + " VNĐ");
    }
    
    public void hienThiSoLuongNhapThuocTheoNgay(){
        
        ArrayList<ThongKeSoLuongXuatNhapThuocTheoNgay> dsXuatNhapThuocTheoNgay = null;
        ThongKeSoLuongXuatNhapThuocTheoNgay thuocTheoNgay = new ThongKeSoLuongXuatNhapThuocTheoNgay();
        
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        thuocTheoNgay.setNgayGioXuat(simpleDateFormat.format(txtNgay.getDate()));
        
        ThongKeSoLuongXuatNhapThuocTheoNgayService thongKeSoLuongXuatNhapThuocTheoNgayService = new ThongKeSoLuongXuatNhapThuocTheoNgayService();
        dsXuatNhapThuocTheoNgay = thongKeSoLuongXuatNhapThuocTheoNgayService.laySoLuongNhapThuocTheoNgay(thuocTheoNgay);

        //fotmat number
        NumberFormat currentLocale = NumberFormat.getInstance();
        Locale localeVN = new Locale("vi", "VN");
        NumberFormat vn = NumberFormat.getInstance(localeVN);
        
        int tongDoanhThuNgay =0;
        modelNhap.setColumnCount(3);
        modelNhap.setRowCount(0);
        for (ThongKeSoLuongXuatNhapThuocTheoNgay thongKeSoLuongXuatNhapThuocTheoNgay : dsXuatNhapThuocTheoNgay) {
            Vector row = new Vector();
            row.add(thongKeSoLuongXuatNhapThuocTheoNgay.getSoChungTuXuat());
            row.add(thongKeSoLuongXuatNhapThuocTheoNgay.getTenNhaCungCap());
            row.add(vn.format(thongKeSoLuongXuatNhapThuocTheoNgay.getTongTienNhap()));
            
            dataNhap.add(row);
        }
        
        int tongTienSoLuongNhap = 0;
        dsXuatNhapThuocTheoNgay = thongKeSoLuongXuatNhapThuocTheoNgayService.layTongSoLuongNhapThuocTheoNgay(thuocTheoNgay);
        for (ThongKeSoLuongXuatNhapThuocTheoNgay thongKeSoLuongXuatNhapThuocTheoNgay1 : dsXuatNhapThuocTheoNgay) {
            tongTienSoLuongNhap = thongKeSoLuongXuatNhapThuocTheoNgay1.getTongTienNhap();
        }
        
        lblTongDoanhThuNgay.setText("Tổng chi ngày "+simpleDateFormat.format(txtNgay.getDate()) + " là: " + vn.format(tongTienSoLuongNhap) + " VNĐ");
        
    }
    
