    private void btbLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btbLuuActionPerformed

        TrangChuUI trangChuUI = new TrangChuUI();
        Lo lo = new Lo();
        //ma lo
        if (trangChuUI.checkDoDai(txtMaLo.getText(), 1, 5)) {
            lo.setMaLo(txtMaLo.getText().trim());
        }else{
            JOptionPane.showMessageDialog(null, "Mã lô không hợp lệ");
            txtMaLo.requestFocus();
            return;
        }
        
        //so chung tu nhap
        lo.setSoChungTuNhap(Integer.parseInt(cbSoChungTuNhap.getSelectedItem().toString()));
        
        //Ma thuoc
        lo.setMaThuoc(trangChuUI.ngatChuoi((String) cbMaThuoc.getSelectedItem(), "_", 0));
        
        //ngay san xuat
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        if (txtNgaySanXuat.getDate() != null) {
            lo.setNgaySanXuat(simpleDateFormat.format(txtNgaySanXuat.getDate()));
        }else{
            JOptionPane.showMessageDialog(null, "Ngày sản xuất không được trống");
            txtNgaySanXuat.requestFocus();
            return;
        }
        
        //Hạn sử dụng
        if (txtHanSuDung.getDate() != null) {
            lo.setHanSuDung(simpleDateFormat.format(txtHanSuDung.getDate()));
        }else{
            JOptionPane.showMessageDialog(null, "Hạn sử dụng không được trống");
            txtHanSuDung.requestFocus();
            return;
        }
        
        //gia ban
        if (trangChuUI.checkReg(txtGiaBan.getText(), "\\d{1,11}")) {
            lo.setGiaBan(Integer.parseInt(txtGiaBan.getText().trim()));
        }else{
            JOptionPane.showMessageDialog(null, "Giá bán không hợp lệ");
            txtGiaBan.requestFocus();
            return;
        }
        
        //so luong
        if (trangChuUI.checkReg(txtSoLuong.getText(), "\\d{1,11}")) {
            lo.setSoLuong(Integer.parseInt(txtSoLuong.getText().trim()));
        }else{
            JOptionPane.showMessageDialog(null, "Số lượng không hợp lệ");
            txtSoLuong.requestFocus();
            return;
        }
        
        //don gia von
        if (trangChuUI.checkReg(txtDonGiaVon.getText(), "\\d{1,11}")) {
            lo.setDonGiaVon(Integer.parseInt(txtDonGiaVon.getText().trim()));
        }else{
            JOptionPane.showMessageDialog(null, "Đơn giá vốn không hợp lệ");
            txtDonGiaVon.requestFocus();
            return;
        }
        
        
        //Thuc hien them lo
        LoService loService = new LoService();
        int result = loService.themLo(lo);
        if (result>0) {
            hienThiToanBoLo();
            JOptionPane.showMessageDialog(null, "Đã thêm");
        }else{
            JOptionPane.showMessageDialog(null, "Thêm thất bại. Vui lòng kiểm tra lại");
            return;
        }

    }//GEN-LAST:event_btbLuuActionPerformed
    
