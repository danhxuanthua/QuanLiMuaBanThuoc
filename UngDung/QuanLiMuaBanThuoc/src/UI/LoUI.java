    private void tbLoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbLoMouseClicked
        // TODO add your handling code here:
        int row = tbLo.getSelectedRow();
        
        String soChungTuNhap = tbLo.getValueAt(row, 0).toString();
        maLo  = (String) tbLo.getValueAt(row, 1);
        String maThuocVaTenThuoc = (String) tbLo.getValueAt(row, 2);
        String ngaySanXuat = (String) tbLo.getValueAt(row, 3);
        String hanSuDung = (String) tbLo.getValueAt(row, 4);
        String giaBan = tbLo.getValueAt(row, 5).toString();
        String soLuong =  tbLo.getValueAt(row, 6).toString();
        String donGiaVon = tbLo.getValueAt(row, 7).toString();
        
        hienThiSoChungTuNhapSua();
        hienThiTenThuocSua();
        cbSoChungTuNhapSua.setSelectedItem(soChungTuNhap);
        txtMaLoSua.setText(maLo);
        cbMaThuocSua.setSelectedItem(maThuocVaTenThuoc);
        
        try {
            Date fmNgaySanXuat = new SimpleDateFormat("yyyy-MM-dd").parse(ngaySanXuat);
            txtNgaySanXuatSua.setDate(fmNgaySanXuat);
            Date fmHanSuDung = new SimpleDateFormat("yyyy-MM-dd").parse(hanSuDung);
            txtHanSuDungSua.setDate(fmHanSuDung);
        } catch (ParseException ex) {
            Logger.getLogger(HoaDonNhapUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        txtGiaBanSua.setText(giaBan.replace(".", ""));
        txtSoLuongSua.setText(soLuong);
        txtDonGiaVonSua.setText(donGiaVon.replace(".", ""));
        
    }//GEN-LAST:event_tbLoMouseClicked

