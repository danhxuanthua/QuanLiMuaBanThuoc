    public void hienThiToanBoLo(){
        LoService loService = new LoService();
        dsLo = loService.layToanBoLo();
        
        //fotmat number 
        NumberFormat currentLocale = NumberFormat.getInstance();
        Locale localeVN = new Locale("vi", "VN");
        NumberFormat vn = NumberFormat.getInstance(localeVN);
        
        model.setRowCount(0);
        for (Lo lo : dsLo) {
            Vector row = new Vector();
            row.add(lo.getSoChungTuNhap());
            row.add(lo.getMaLo());
            row.add(lo.getMaThuoc()+"_"+lo.getTenThuoc());
            row.add(lo.getNgaySanXuat());
            row.add(lo.getHanSuDung());
            row.add(vn.format(lo.getGiaBan()));
            row.add(lo.getSoLuong());
            row.add(vn.format(lo.getDonGiaVon()));
            
            data.add(row);
        }
    }
    
