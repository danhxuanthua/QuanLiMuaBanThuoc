    private void btnXoaLoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaLoActionPerformed
        // TODO add your handling code here:
        if (maLo == null) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn lô thuốc cần xóa");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa?", "Xác nhận", JOptionPane.YES_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        Lo lo = new Lo();
        lo.setMaLo(maLo);
        LoService loService = new LoService();
        int result = loService.xoaLo(lo);
        if (result > 0) {
            hienThiToanBoLo();
            JOptionPane.showMessageDialog(null, "Đã xóa");
        }else{
            JOptionPane.showMessageDialog(null, "Xóa thất bại");
        }
    }//GEN-LAST:event_btnXoaLoActionPerformed

