    public void hienThiInHoaDon(){
        
            HoaDonXuat hoaDonXuat  = new HoaDonXuat();
            hoaDonXuat.setSoChungTuXuat(Integer.parseInt(soChungTuXuat));
            
            InHoaDonXuatService inHoaDonXuatService = new InHoaDonXuatService();
            dsInHoaDonXuat = inHoaDonXuatService.layThongTinInHoaDonXuat(hoaDonXuat);
            
             //fotmat number 
            NumberFormat currentLocale = NumberFormat.getInstance();
            Locale localeVN = new Locale("vi", "VN");
            NumberFormat vn = NumberFormat.getInstance(localeVN);
            
            modelIn.setRowCount(0);
            int soChungTuXuatIn=0;
            String ngayGioXuatIn = null;
            String hoIn = null;
            String tenIn = null;
            int tongTienbanIn = 0;
            
            for (InHoaDonXuat inHoaDonXuat : dsInHoaDonXuat) {
                soChungTuXuatIn = inHoaDonXuat.getSoChungTuXuat();
                hoIn = inHoaDonXuat.getHo();
                tenIn = inHoaDonXuat.getTen();
                tongTienbanIn = inHoaDonXuat.getTongTienban();
                ngayGioXuatIn = inHoaDonXuat.getNgayGioXuat();
                
                Vector row = new Vector<Object>();
                row.add(inHoaDonXuat.getTenThuoc());
                row.add(inHoaDonXuat.getSoLuong());
                row.add(vn.format(inHoaDonXuat.getGiaBanDuaVaoSoLuong()));
                dataIn.add(row);
            }
            lblSoChungTuXuat.setText("Số hóa đơn: "+soChungTuXuatIn);
            lblNhanVien.setText("Nhân viên: "+hoIn+" "+tenIn);
            lblNgayGioXuat.setText("Ngày lập: " + ngayGioXuatIn);
            lblTongTien.setText("Tổng tiền: " + vn.format(tongTienbanIn)+" VNĐ");
            TrangChuUI trangChuUI = new TrangChuUI();
            String strTongTienBan = tongTienbanIn+"";
            ArrayList<String> kq = trangChuUI.readNum(strTongTienBan);
            String docThanhChu = "";
            for (int i = 0; i < kq.size(); i++) {
                docThanhChu += kq.get(i)+ " ";
            }
            txtDocThanhChu.setText("("+ docThanhChu +")");
    }
    
