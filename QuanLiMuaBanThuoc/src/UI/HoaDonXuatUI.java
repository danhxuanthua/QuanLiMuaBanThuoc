    private void btbLuuHoaDonXuatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btbLuuHoaDonXuatActionPerformed
        //Lay ma nhan vien
        TrangChuUI trangChuUI = new TrangChuUI();
        trangChuUI.layThongTinNhanVien();
        int maNV = trangChuUI.maNV;

        HoaDonXuat hoaDonXuat = new HoaDonXuat();

        //ma nhan vien
        hoaDonXuat.setMaNhanVien(maNV);

        //check thoi gian nhap
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        if (txtNgayGioXuat.getDate()!= null){
            hoaDonXuat.setNgayGioXuat(simpleDateFormat.format(txtNgayGioXuat.getDate()));
        }else{
            JOptionPane.showMessageDialog(null, "Thời gian nhập không hơp lệ.");
            return;
        }

        //check hoa don nhap
        if (trangChuUI.checkReg(txtTongTienXuat.getText(), "\\d{3,10}")) {
            hoaDonXuat.setTongTienBan(Integer.parseInt(txtTongTienXuat.getText().trim()));
        }else{
            JOptionPane.showMessageDialog(null, "Tổng tiền nhập phải là số dộ dài từ 3-10");
            txtTongTienXuat.requestFocus();
            return;
        }

        //Thuc hien them hoa don
        HoaDonXuatService hoaDonXuatService = new HoaDonXuatService();
        int result = hoaDonXuatService.themHoaDonXuat(hoaDonXuat);
        if (result>0) {
            hienThiToanBoHoaDonXuat();
            JOptionPane.showMessageDialog(null, "Hóa đơn đã được tạo, Vui lòng thêm chi tiết cho hóa đơn!");
        }else{
            JOptionPane.showMessageDialog(null, "Tạo hóa đơn thất bại. Vui lòng kiểm tra lại");
            return;
        }

    }//GEN-LAST:event_btbLuuHoaDonXuatActionPerformed

