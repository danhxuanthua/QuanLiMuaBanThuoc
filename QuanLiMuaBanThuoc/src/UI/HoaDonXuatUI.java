    private void btnXoaHoaDonXuatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaHoaDonXuatActionPerformed
        // TODO add your handling code here:
        if (soChungTuXuat == null) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn hóa đơn cần xóa");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(null, "Ban chắc chắn muốn xóa?", "Xác nhận", JOptionPane.YES_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {

            //Thuc hien hoa
            HoaDonXuat hoaDonXuat = new HoaDonXuat();
            hoaDonXuat.setSoChungTuXuat(Integer.parseInt(soChungTuXuat));
            HoaDonXuatService hoaDonXuatService = new HoaDonXuatService();

            int result = hoaDonXuatService.xoaHoaDon(hoaDonXuat);
            if (result > 0) {
                hienThiToanBoHoaDonXuat();
                JOptionPane.showMessageDialog(null, "Đã xóa");
            }else{
                JOptionPane.showMessageDialog(null, "Hóa đơn đang được sử dụng không thể xóa, Vui lòng kiểm tra lại");
                return;
            }
        }else{
            return;
        }

    }//GEN-LAST:event_btnXoaHoaDonXuatActionPerformed

