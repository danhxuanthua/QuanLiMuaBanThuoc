    ArrayList<HoaDonXuat> dsHoaDonXuat = null;
    public void hienThiToanBoHoaDonXuat(){
        HoaDonXuatService hoaDonXuatService = new HoaDonXuatService();
        dsHoaDonXuat = hoaDonXuatService.layToanToanBoHoaXuat();

        model.setRowCount(0);
        //fotmat number
        NumberFormat currentLocale = NumberFormat.getInstance();
        Locale localeVN = new Locale("vi", "VN");
        NumberFormat vn = NumberFormat.getInstance(localeVN);

        for (HoaDonXuat hoaDonXuat : dsHoaDonXuat) {
            Vector row = new Vector();
            row.add(hoaDonXuat.getSoChungTuXuat());
            row.add(hoaDonXuat.getHo()+" "+hoaDonXuat.getTen());
            row.add(hoaDonXuat.getNgayGioXuat());
            row.add(vn.format(hoaDonXuat.getTongTienBan()));
            data.add(row);
        }
    }

   


