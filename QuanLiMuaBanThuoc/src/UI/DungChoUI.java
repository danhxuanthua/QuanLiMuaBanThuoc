    ArrayList<DungCho> dsDungCho =null;
    public void hienThiToanBoDungCho(){
        DungChoService dungChoService = new DungChoService();
        dsDungCho = dungChoService.layToanBoDungCho();
        
        model.setRowCount(0);
        for (DungCho dungCho : dsDungCho) {
            Vector row = new Vector();
            row.add(dungCho.getMaThuoc() +"_"+dungCho.getTenThuoc());
            row.add(dungCho.getMaDoiTuongBenhNhan()+"-"+dungCho.getTenDoiTuongBenhNhan());
            
            data.add(row);
        }
    }
    
    public void hienThiToanBoThuoc(){
        ArrayList<Thuoc> dsThuoc = null;
        ThuocService thuocService  = new ThuocService();
        dsThuoc = thuocService.layToanBoThongTinThuoc();
        
        for (Thuoc thuoc : dsThuoc) {
            cbThuoc.addItem(thuoc.getMaThuoc()+"_"+thuoc.getTenThuoc());
        }
        
    }
    
