    private void btnXoaDungChoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaDungChoActionPerformed
        // TODO add your handling code here:
        if (maThuoc == null && maDoiTuongBenhNhan == null) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn thuốc cần xóa");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa?", "Xác nhận", JOptionPane.YES_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        DungCho dungCho = new DungCho();
        dungCho.setMaThuoc(maThuoc);
        dungCho.setMaDoiTuongBenhNhan(Integer.parseInt(maDoiTuongBenhNhan));

        DungChoService dungChoService = new DungChoService();
        int result = dungChoService.xoaDungCho(dungCho);
        if (result > 0) {
            hienThiToanBoDungCho();
            JOptionPane.showMessageDialog(null, "Đã xóa");
        }else{
            JOptionPane.showMessageDialog(null, "Xóa thất bại");
        }
    }//GEN-LAST:event_btnXoaDungChoActionPerformed

