    private void btbLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btbLuuActionPerformed
        // TODO add your handling code here:
        TrangChuUI trangChuUI = new TrangChuUI();
        DungCho dungCho = new DungCho();

        //Ma thuoc
        String maThuoc = trangChuUI.ngatChuoi(cbThuoc.getSelectedItem().toString(), "_", 0);
        dungCho.setMaThuoc(maThuoc);
        //Ma doi tuong benh nhan
        String maDoiTuongBenhNhan = trangChuUI.ngatChuoi(cbDoiTuongBenhNhan.getSelectedItem().toString() , "-", 0);
        dungCho.setMaDoiTuongBenhNhan(Integer.parseInt(maDoiTuongBenhNhan));
        
        DungChoService dungChoService = new DungChoService();
        int result = dungChoService.themDungCho(dungCho);
        if (result > 0) {
            hienThiToanBoDungCho();
            JOptionPane.showMessageDialog(null, "Thêm thành công");
        }else{
            JOptionPane.showMessageDialog(null, "Thuốc dùng cho đối tượng bệnh nhân đã được thêm trước đó. Vui lòng kiêm tra lại");
        }
    }//GEN-LAST:event_btbLuuActionPerformed

