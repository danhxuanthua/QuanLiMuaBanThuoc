    private void btnXoaNhomThuocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaNhomThuocActionPerformed
        // TODO add your handling code here:
        if (maNhom == null) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn nhóm thuốc cần xóa");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa?", "Xác nhận", JOptionPane.YES_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        
        NhomThuoc nhomThuoc = new NhomThuoc();
        nhomThuoc.setMaNhom(Integer.parseInt(maNhom));
        NhomThuocService nhomThuocService = new NhomThuocService();
        int result = nhomThuocService.xoaNhomThuoc(nhomThuoc);
        if (result > 0) {
            hienThiToanBoNhomThuoc();
            JOptionPane.showMessageDialog(null, "Đã xóa");
        }else{
            JOptionPane.showMessageDialog(null, "Xóa thất bại");
        }

    }//GEN-LAST:event_btnXoaNhomThuocActionPerformed

