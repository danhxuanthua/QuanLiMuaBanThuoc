    private void setTable(){
        header.add("Mã nhóm");
        header.add("Tên nhóm thuốc");
     
        model = (DefaultTableModel) tbNhomThuoc.getModel();    
        model.setDataVector(data, header);
    }
    
    public void hienThiToanBoNhomThuoc(){
        
        NhomThuocService nhomThuocService = new NhomThuocService();
        dsNhomThuoc = nhomThuocService.layToanBoNhomThuoc();
        
        model.setRowCount(0);
        for (NhomThuoc nhomThuoc : dsNhomThuoc) {
            Vector row = new Vector();
            row.add(nhomThuoc.getMaNhom());
            row.add(nhomThuoc.getTenNhom());
            data.add(row);
        }
        
    }
    
    
    
