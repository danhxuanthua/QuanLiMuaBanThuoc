    private void btbLuuNhomThuocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btbLuuNhomThuocActionPerformed
        // TODO add your handling code here:
        TrangChuUI trangChuUI = new TrangChuUI();
        NhomThuoc nhomThuoc = new NhomThuoc();
       
        if (trangChuUI.checkDoDai(txtTenNhomThuoc.getText(), 1, 50)) {
            nhomThuoc.setTenNhom(txtTenNhomThuoc.getText().trim());
        }else{
            JOptionPane.showMessageDialog(null, "Tên nhóm thuốc không hợp lệ");
            txtTenNhomThuoc.requestFocus();
            return;
        }
       
       NhomThuocService nhomThuocService = new NhomThuocService();
       int result = nhomThuocService.themNhomThuoc(nhomThuoc);
        if (result > 0) {
            hienThiToanBoNhomThuoc();
            JOptionPane.showMessageDialog(null, "Thêm thành công");
        }else{
            JOptionPane.showMessageDialog(null, "Nhóm thuốc đã tồn tài. Vui lòng kiêm tra lại");
            return;
        }
               

    }//GEN-LAST:event_btbLuuNhomThuocActionPerformed

