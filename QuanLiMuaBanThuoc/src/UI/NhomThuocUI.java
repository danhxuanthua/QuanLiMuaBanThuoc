    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        TrangChuUI trangChuUI = new TrangChuUI();
        NhomThuoc nhomThuoc = new NhomThuoc();
        nhomThuoc.setMaNhom(Integer.parseInt(maNhom));
        
        if (trangChuUI.checkDoDai(txtTenNhomThuocSua.getText(), 1, 50)) {
            
            nhomThuoc.setTenNhom(txtTenNhomThuocSua.getText().trim());
        }else{
            JOptionPane.showMessageDialog(null, "Tên nhóm thuốc không hợp lệ");
            return; 
        }
        
        NhomThuocService nhomThuocService = new NhomThuocService();
        int result = nhomThuocService.suaNhomThuoc(nhomThuoc);
        if (result > 0) {
            hienThiToanBoNhomThuoc();
            JOptionPane.showMessageDialog(null, "Đã cập nhật");
        }else{
            JOptionPane.showMessageDialog(null, "Nhóm thuốc đã tồn tại. Vui lòng kiêm tra lại");
            return;
                   
        }
        
    }//GEN-LAST:event_btnCapNhatActionPerformed

