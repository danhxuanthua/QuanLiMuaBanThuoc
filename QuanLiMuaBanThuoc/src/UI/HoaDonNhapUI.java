    private void btnXoaHoaDonNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaHoaDonNhapActionPerformed
        // TODO add your handling code here:
        if (soChungTuNhap == null) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn hóa đơn cần xóa");
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(null, "Ban chắc chắn muốn xóa?", "Xác nhận", JOptionPane.YES_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            //Thuc hien hoa
            HoaDonNhap hoaDonNhap = new HoaDonNhap();
            hoaDonNhap.setSoChungTuNhap(Integer.parseInt(soChungTuNhap));
            HoaDonNhapService hoaDonNhapService = new HoaDonNhapService();
            
            int result = hoaDonNhapService.xoaHoaDon(hoaDonNhap);
            if (result > 0) {
                hienThiToanBoHoaDonNhap();
                JOptionPane.showMessageDialog(null, "Đã xóa");
            }else{
               JOptionPane.showMessageDialog(null, "Hóa đơn đang được sử dụng không thể xóa. Vui lòng kiểm tra lại");
               return;
            }
        }else{
            
            return;
        }        
        
    }//GEN-LAST:event_btnXoaHoaDonNhapActionPerformed

