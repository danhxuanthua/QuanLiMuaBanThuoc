    private void tbHoaDonNhapThuocMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbHoaDonNhapThuocMouseClicked
        // TODO add your handling code here:
        int row = tbHoaDonNhapThuoc.getSelectedRow();
        soChungTuNhap = tbHoaDonNhapThuoc.getValueAt(row, 0).toString();
        
        //Ngay gio nhap
        try {
            Date ngayGioNhap = new SimpleDateFormat("yyyy-MM-dd").parse(tbHoaDonNhapThuoc.getValueAt(row, 2).toString());
            txtNgayGioNhapSua.setDate(ngayGioNhap);
        } catch (ParseException ex) {
            Logger.getLogger(HoaDonNhapUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        String tenNhaCungCap = (String) tbHoaDonNhapThuoc.getValueAt(row, 3);
        String tongTien = tbHoaDonNhapThuoc.getValueAt(row, 4).toString();
        
        //hien thi len form sua
        txtSoChungTuSua.setText(soChungTuNhap);
        txtSoChungTuSua.disable();
        txtTenNhaCungCapSua.setText(tenNhaCungCap);
        txtTongTienNhapSua.setText(tongTien.replace(".", ""));
    }//GEN-LAST:event_tbHoaDonNhapThuocMouseClicked

