    private void btbLuuHoaDonNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btbLuuHoaDonNhapActionPerformed
        //Lay ma nhan vien
        TrangChuUI trangChuUI = new TrangChuUI();
        trangChuUI.layThongTinNhanVien();
        int maNV = trangChuUI.maNV;
        
        HoaDonNhap hoaDonNhap = new HoaDonNhap();
        
        //ma nhan vien
        hoaDonNhap.setMaNhanVien(maNV);
        
        //check thoi gian nhap
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        if (txtNgayGioNhap.getDate()!= null){
            hoaDonNhap.setNgayGioNhap(simpleDateFormat.format(txtNgayGioNhap.getDate()));
        }else{
            JOptionPane.showMessageDialog(null, "Thời gian nhập không hơp lệ.");
            return;
        }
        
        //check ten nha cung cap
        if (trangChuUI.checkDoDai(txtTenNhaCungCap.getText(),2 ,200)) {
            hoaDonNhap.setTenNhaCungCap(txtTenNhaCungCap.getText().trim());
        }else{
            JOptionPane.showMessageDialog(null, "Tên nhà cung cấp phải có độ dài từ 2 - 50 ký tự");
            txtTenNhaCungCap.requestFocus();
            return;
        }
        
        //check hoa don nhap
        if (trangChuUI.checkReg(txtTongTienNhap.getText(), "\\d{3,10}")) {
            hoaDonNhap.setTongTienNhap(Integer.parseInt(txtTongTienNhap.getText().trim()));            
        }else{
            JOptionPane.showMessageDialog(null, "Tổng tiền nhập phải là số dộ dài từ 3-10");
            txtTongTienNhap.requestFocus();
            return;
        }
        
        //Thuc hien them hoa don
        HoaDonNhapService hoaDonNhapService = new HoaDonNhapService();
        int result = hoaDonNhapService.themHoaDonNhap(hoaDonNhap);
        if (result>0) {
            hienThiToanBoHoaDonNhap();
            JOptionPane.showMessageDialog(null, "Đã thêm một hóa đơn");
        }else{
            JOptionPane.showMessageDialog(null, "Thêm thất bại. Vui lòng kiểm tra lại");
            return;
        }
        
    }//GEN-LAST:event_btbLuuHoaDonNhapActionPerformed

