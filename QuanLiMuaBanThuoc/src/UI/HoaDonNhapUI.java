    //hien thi toan bo hoa don nhap
    ArrayList<HoaDonNhap> dsHoaDonNhap = null;
    public void hienThiToanBoHoaDonNhap(){
        HoaDonNhapService hoaDonNhapService = new HoaDonNhapService();
        dsHoaDonNhap = hoaDonNhapService.layToanToanBoHoaDon();
        
        model.setRowCount(0);
        //fotmat number 
        NumberFormat currentLocale = NumberFormat.getInstance();
        Locale localeVN = new Locale("vi", "VN");
        NumberFormat vn = NumberFormat.getInstance(localeVN);
        
        for (HoaDonNhap hoaDonNhap : dsHoaDonNhap) {
            Vector row = new Vector();
            row.add(hoaDonNhap.getSoChungTuNhap());
            row.add(hoaDonNhap.getHo()+" "+hoaDonNhap.getTen());
            row.add(hoaDonNhap.getNgayGioNhap());
            row.add(hoaDonNhap.getTenNhaCungCap());
            row.add(vn.format(hoaDonNhap.getTongTienNhap()));
            data.add(row);
        }
    }
    
    
    
