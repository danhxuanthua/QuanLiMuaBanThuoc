    private void setTableXuat(){
        headerXuat.add("Mã lô");
        headerXuat.add("Số chứng từ nhập");
        headerXuat.add("Tên thuốc");
        headerXuat.add("Ngày sản xuất");
        headerXuat.add("Hạn sử dụng");
        headerXuat.add("Số lượng");
        
        modelXuat = (DefaultTableModel) tbThongKeThuocHetHan.getModel();    
        modelXuat.setDataVector(dataXuat, headerXuat);
        
    }
    
    public void hienThiDanhSachThuocSapHetHanSuDung(){
        ArrayList<ThongKeDanhSachThuocSapHetHanSuDung> dsHanSuDungs= null;
        ThongKeDanhSachThuocSapHetHanService thongKeDanhSachThuocSapHetHanService = new ThongKeDanhSachThuocSapHetHanService();
        dsHanSuDungs = thongKeDanhSachThuocSapHetHanService.layDanhSachThuocSapHetHan();
        for (ThongKeDanhSachThuocSapHetHanSuDung hetHanSuDung1 : dsHanSuDungs) {
            Vector row = new Vector();
            row.add(hetHanSuDung1.getMaLo());
            row.add(hetHanSuDung1.getSoChungTuNhap());
            row.add(hetHanSuDung1.getTenThuoc());
            row.add(hetHanSuDung1.getNgaySanXuat());
            row.add(hetHanSuDung1.getHanSuDung());
            row.add(hetHanSuDung1.getSoLuong());
            
            dataXuat.add(row);
        }
    }
    
