    public void hienThiSoLuongNhapThuocTheoThang(){
        
        ArrayList<ThongKeSoLuongXuatNhapThuocTheoThang> dsXuatNhapThuocTheoThang = null;
        ThongKeSoLuongXuatNhapThuocTheoThang thuocTheoThang = new ThongKeSoLuongXuatNhapThuocTheoThang();
        
        thuocTheoThang.setNgayGioXuat((String) txtThang.getSelectedItem());
        thuocTheoThang.setNamNhap(txtNam.getYear()+"");
        ThongKeSoLuongXuatNhapThuocTheoThangService thongKeSoLuongXuatNhapThuocTheoThangService = new ThongKeSoLuongXuatNhapThuocTheoThangService();
        dsXuatNhapThuocTheoThang = thongKeSoLuongXuatNhapThuocTheoThangService.laySoLuongNhapThuocTheoThangNam(thuocTheoThang);

        //fotmat number
        NumberFormat currentLocale = NumberFormat.getInstance();
        Locale localeVN = new Locale("vi", "VN");
        NumberFormat vn = NumberFormat.getInstance(localeVN);
        
        System.out.println(txtNam.getYear());
        
        modelNhap.setColumnCount(3);
        modelNhap.setRowCount(0);
        for (ThongKeSoLuongXuatNhapThuocTheoThang thongKeSoLuongXuatNhapThuocTheoThang : dsXuatNhapThuocTheoThang) {
            Vector row = new Vector();
            row.add(thongKeSoLuongXuatNhapThuocTheoThang.getSoChungTuXuat());
            row.add(thongKeSoLuongXuatNhapThuocTheoThang.getTenNhaCungCap());
            row.add(vn.format(thongKeSoLuongXuatNhapThuocTheoThang.getTongTienNhap()));
            
            dataNhap.add(row);
        }
        
        int tongTienSoLuongNhap = 0;
        dsXuatNhapThuocTheoThang = thongKeSoLuongXuatNhapThuocTheoThangService.layTongSoLuongNhapThuocTheoThangNam(thuocTheoThang);
        for (ThongKeSoLuongXuatNhapThuocTheoThang thongKeSoLuongXuatNhapThuocTheoThang1 : dsXuatNhapThuocTheoThang) {
            tongTienSoLuongNhap = thongKeSoLuongXuatNhapThuocTheoThang1.getTongTienNhap();
        }
        
        lblTongDoanhThuNgay.setText("Tổng chi tháng "+txtThang.getSelectedItem() + " là: " + vn.format(tongTienSoLuongNhap) + " VNĐ");
        
    }
    
    
