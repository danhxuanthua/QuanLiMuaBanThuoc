    private void btnXoaBenhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaBenhActionPerformed
        // TODO add your handling code here:
        
        if (maBenh == null) {
            JOptionPane.showMessageDialog(null, "Bạn cần chọn bệnh để xóa");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa?", "Xác nhận", JOptionPane.YES_OPTION);
        if ( confirm != JOptionPane.YES_OPTION) {
            return;
        }
        
        Benh benh = new Benh();
        benh.setMaBenh(Integer.parseInt(maBenh));
        BenhService benhService = new BenhService();
        int result = benhService.xoaBenh(benh);
        if (result >0) {
            hienThiToanBoBenh();
            JOptionPane.showMessageDialog(null, "Đã xóa");
        }else{
            JOptionPane.showMessageDialog(null, "Xóa thất bại");
        }
    }//GEN-LAST:event_btnXoaBenhActionPerformed

