    private void btbLuuBenhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btbLuuBenhActionPerformed
        // TODO add your handling code here:
        TrangChuUI trangChuUI = new TrangChuUI();
        
        Benh benh = new Benh();
        //check ten benh
        if (trangChuUI.checkDoDai(txtTenBenh.getText(), 1, 50)) {
            benh.setTenBenh(txtTenBenh.getText());
        }else{
            JOptionPane.showMessageDialog(null, "Tên bệnh không hợp lệ");
            txtTenBenh.requestFocus();
            return;
        }
        
        BenhService benhService = new BenhService();
        int result = benhService.themBenh(benh);
        if (result > 0) {
            hienThiToanBoBenh();
            JOptionPane.showMessageDialog(null, "Đã thêm");
        }else{
            JOptionPane.showMessageDialog(null, "Tên bệnh đã tồn tại");
        }

    }//GEN-LAST:event_btbLuuBenhActionPerformed

